    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            try {
            // Path ke file laporan JasperReport
            String reportPath = "src/Report/LaporanTransaksi.jasper";

            // Membuka koneksi ke database MySQL
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/buku", "root", "");

            // Membuat objek HashMap untuk menyimpan parameter yang akan digunakan dalam laporan
            HashMap<String, Object> parameters = new HashMap<>();

            // Mengisi dan menampilkan laporan menggunakan JasperReports
            JasperPrint print = JasperFillManager.fillReport(reportPath, parameters, conn);

            // Membuat objek JasperViewer untuk menampilkan laporan
            JasperViewer viewer = new JasperViewer(print, false);

            // Menampilkan viewer laporan
            viewer.setVisible(true);
        } catch (Exception e) {
            // Menangani kesalahan yang mungkin terjadi saat menampilkan laporan
            JOptionPane.showMessageDialog(this, "Error displaying report: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
       // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
   // Assuming JKalender is a date picker component
// Mengambil nilai dari JTextField atau komponen lainnya yang sesuai
String Nama_pembeli = nmPembeli.getText();
String harga = txtHarga.getText();
int jumlah = Integer.parseInt(JM.getText());
String total = TTOTAL.getText();

// Mengambil nilai tanggal
Date selectedDate = JKalender.getDate();
SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
String formattedDate = sdf.format(selectedDate);

try {
    Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/buku", "root", "");

    String sql = "INSERT INTO transaksi (id_transaksi, Nama_pembeli, tanggal, harga, jumlah, total) VALUES (NULL, ?, ?, ?, ?, ?)";
    try (PreparedStatement preparedStatement = connection.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS)) {
        preparedStatement.setString(1, Nama_pembeli);
        preparedStatement.setString(2, formattedDate);
        preparedStatement.setString(3, harga);
        preparedStatement.setInt(4, jumlah);
        preparedStatement.setString(5, total);

        int affectedRows = preparedStatement.executeUpdate();
        if (affectedRows > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan.");
        } else {
            JOptionPane.showMessageDialog(null, "Gagal menyimpan data.");
        }
    }

    connection.close();
} catch (SQLException ex) {
    ex.printStackTrace();
    JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + ex.getMessage());
}




    }//GEN-LAST:event_jButton3ActionPerformed

