    private void TbTransaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TbTransaksiMouseClicked
      int selectedRow = TbTransaksi.getSelectedRow();

// Pemeriksaan apakah baris yang dipilih valid
if (selectedRow >= 0 && selectedRow < TbTransaksi.getRowCount()) {
    // Pastikan bahwa kolom yang Anda akses ada dalam batas indeks yang valid
    int hargaColumnIndex = 3;
    int jumlahColumnIndex = 4;
    int totalColumnIndex = 5;

    if (hargaColumnIndex < TbTransaksi.getColumnCount()) {
        txtHarga.setText(TbTransaksi.getModel().getValueAt(selectedRow, hargaColumnIndex).toString());
    }

    if (jumlahColumnIndex < TbTransaksi.getColumnCount()) {
        JM.setText(TbTransaksi.getModel().getValueAt(selectedRow, jumlahColumnIndex).toString());
    }

    if (totalColumnIndex < TbTransaksi.getColumnCount()) {
        TTOTAL.setText(TbTransaksi.getModel().getValueAt(selectedRow, totalColumnIndex).toString());
    }
} else {
    // Tindakan yang sesuai jika baris yang dipilih tidak valid
    // Misalnya, menampilkan pesan kesalahan atau melakukan tindakan lainnya.
}
       
    }//GEN-LAST:event_TbTransaksiMouseClicked

